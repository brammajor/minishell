#include "minishell.h"

int	ft_che(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 9 && str[i] <= 13) && !(str[i] == ' '))
			return (0);
		i++;
	}
	return (1);
}
